  getNormalizedUserFeatures() {
    const seen = new Set<string>();
    const values: string[] = [];

    for (const control of this.featureArray.controls) {
      const raw = String(control.get('value')?.value ?? '').trim();
      if (!raw) {
        continue;
      }

      const normalized = raw.toLowerCase();
      if (seen.has(normalized)) {
        continue;
      }

      seen.add(normalized);
      values.push(raw);
    }

    return values;
  }

  async onSourceFileSelected(index: number, event: Event) {
    const input = event.target as HTMLInputElement | null;
    const files = Array.from(input?.files ?? []);
    if (!files.length) {
      return;
    }

    const group = this.sourceArray.at(index);
    const sourceKind = (group.get('kind')?.value as SourceKind | undefined) ?? 'file';

    try {
      const preparedFiles = await Promise.all(files.map((file) => this.buildFileBackedSource(sourceKind, file)));
      const [primaryFile, ...additionalFiles] = preparedFiles;

      group.patchValue(primaryFile);

      additionalFiles.forEach((source, offset) => {
        this.sourceArray.insert(index + offset + 1, this.createSourceGroup(source));
      });

      this.expandedSourceIndex.set(index);
      requestAnimationFrame(() => this.ensureSourceVisible(index));
    } catch {
      this.notifications.error('Unable to read the selected file.');
    } finally {
      if (input) {
        input.value = '';
      }
    }
  }

  submit() {
    if (this.form.controls.title.invalid) {
      this.form.controls.title.markAsTouched();
      this.notifications.error('Enter a suite title before launching generation.');
      return;
    }

    const payload = {
      title: this.form.controls.title.value.trim(),
      description: this.form.controls.description.value.trim(),
      mode: this.form.controls.mode.value,
      sourceInputs: this.getNormalizedSources(),
      userFeatures: this.getNormalizedUserFeatures(),
      suiteContext: this.getSuiteContext(),
      selectedDatasetIds: this.getSelectedDatasetIds(),
      generationOptions: this.getGenerationOptions(),
    };

    this.submitting.set(true);

    this.api
      .createGenerationRun(payload)
      .pipe(takeUntilDestroyed(this.destroyRef))
      .subscribe({
        next: ({ run }) => {
          this.submitting.set(false);
          this.activeRunId.set(run.id);
          this.generationMonitor.trackRun(run);
          const ref = this.snackBar.open(`${payload.title} is now generating.`, 'View runs', {
            duration: 9000,
            panelClass: ['snackbar-success'],
          });

          ref.onAction().pipe(takeUntilDestroyed(this.destroyRef)).subscribe(() => {
            void this.router.navigate(['/test-generator/runs']);
          });
        },
        error: (error) => {
          this.submitting.set(false);
          const message =
            typeof error?.error?.message === 'string'
              ? error.error.message
              : 'Unable to launch test generation right now.';
          this.notifications.error(message);
        },
      });
  }

