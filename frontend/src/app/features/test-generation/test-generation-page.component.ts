  private extractPreviewCases(run: TestGenerationRunSummary | TestGenerationRunDetail | null): RunPreviewCase[] {
    if (!run) {
      return [];
    }

    const detail = 'parsedResponse' in run ? run.parsedResponse : null;
    const record = this.asRecord(detail);
    const cases = Array.isArray(record?.['testCases']) ? record['testCases'] : [];
    if (cases.length) {
      return cases
        .slice(0, 10)
        .map((item) => this.asRecord(item))
        .filter((item): item is Record<string, unknown> => !!item)
        .map((item) => ({
          title: String(item['title'] ?? 'Generated testcase'),
          feature: String(item['feature'] ?? 'Feature coverage'),
          scenario: String(item['scenario'] ?? 'Scenario expansion'),
        }));
    }

    return (run.progress?.previewTitles ?? []).map((title) => ({
      title,
      feature: this.suitePathLabel(),
      scenario: 'Live batch preview',
    }));
  }

  private asRecord(value: unknown): Record<string, unknown> | null {
    return value && typeof value === 'object' && !Array.isArray(value) ? (value as Record<string, unknown>) : null;
  }

  private guessMimeType(fileName: string) {
    if (/\.(html|htm)$/i.test(fileName)) {
      return 'text/html';
    }
    if (/\.xhtml$/i.test(fileName)) {
      return 'application/xhtml+xml';
    }
    if (/\.pdf$/i.test(fileName)) {
      return 'application/pdf';
    }
    if (/\.(doc|docx)$/i.test(fileName)) {
      return 'application/vnd.openxmlformats-officedocument.wordprocessingml.document';
    }
    if (/\.(xls|xlsx)$/i.test(fileName)) {
      return 'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet';
    }
    if (/\.(png|jpg|jpeg|gif|webp)$/i.test(fileName)) {
      return 'image/*';
    }
    return 'application/octet-stream';
  }

  private readFileAsText(file: File) {
    return new Promise<string>((resolve, reject) => {
      const reader = new FileReader();
      reader.onload = () => resolve(String(reader.result ?? ''));
      reader.onerror = () => reject(reader.error);
      reader.readAsText(file);
    });
  }

  private readFileAsDataUrl(file: File) {
    return new Promise<string>((resolve, reject) => {
      const reader = new FileReader();
      reader.onload = () => resolve(String(reader.result ?? ''));
      reader.onerror = () => reject(reader.error);
      reader.readAsDataURL(file);
    });
  }

  private async buildFileBackedSource(kind: SourceKind, file: File): Promise<Partial<GenerationSourceInput>> {
    const isTextLike =
      file.type.startsWith('text/') ||
      /\.(txt|md|csv|json|yaml|yml|xml|html|htm|xhtml)$/i.test(file.name);

    const [dataUrl, contentText] = await Promise.all([
      this.readFileAsDataUrl(file),
      isTextLike ? this.readFileAsText(file) : Promise.resolve(''),
    ]);

    return {
      kind,
      label: file.name,
      filename: file.name,
      mimeType: file.type || this.guessMimeType(file.name),
      dataUrl,
      contentText,
    };
  }

