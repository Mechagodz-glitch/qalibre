  addSource(kind: SourceKind = 'note', options?: { expand?: boolean; scroll?: boolean }) {
    const nextIndex = this.sourceArray.length;
    this.sourceArray.push(this.createSourceGroup({ kind }));

    if (options?.expand ?? true) {
      this.expandedSourceIndex.set(nextIndex);
    }

    if (options?.scroll ?? true) {
      requestAnimationFrame(() => this.ensureSourceVisible(nextIndex));
    }

    return nextIndex;
  }

  addSourceFromTile(kind: SourceKind) {
    this.addSource(kind, { expand: true, scroll: true });
  }

  removeSource(index: number) {
    this.sourceArray.removeAt(index);

    if (!this.sourceArray.length) {
      this.expandedSourceIndex.set(-1);
      return;
    }

    const expandedIndex = this.expandedSourceIndex();
    if (expandedIndex === index) {
      this.expandedSourceIndex.set(Math.min(index, this.sourceArray.length - 1));
      return;
    }

    if (expandedIndex > index) {
      this.expandedSourceIndex.set(expandedIndex - 1);
    }
  }

  addFeature(value = this.featureDraft.value) {
    const trimmed = value.trim();
    if (!trimmed) {
      return;
    }

    const existing = new Set(this.getNormalizedUserFeatures().map((feature) => feature.toLowerCase()));
    if (existing.has(trimmed.toLowerCase())) {
      this.featureDraft.setValue('');
      return;
    }

    this.featureArray.push(this.createFeatureGroup(trimmed));
    this.featureDraft.setValue('');
  }

