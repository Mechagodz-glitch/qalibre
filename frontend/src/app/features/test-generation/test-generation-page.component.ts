  private findProject(projectId: string) {
    return this.projectHierarchy().find((item) => item.id === projectId) ?? null;
  }

  private findModule(moduleId: string) {
    if (!moduleId) {
      return null;
    }

    for (const project of this.projectHierarchy()) {
      const module = project.modules.find((item) => item.id === moduleId);
      if (module) {
        return module;
      }
    }

    return null;
  }

  private findPage(pageId: string) {
    if (!pageId) {
      return null;
    }

    for (const project of this.projectHierarchy()) {
      for (const module of project.modules) {
        const page = module.pages.find((item) => item.id === pageId);
        if (page) {
          return page;
        }
      }
    }

    return null;
  }

  private findFeature(featureId: string) {
    if (!featureId) {
      return null;
    }

    for (const project of this.projectHierarchy()) {
      for (const module of project.modules) {
        for (const page of module.pages) {
          const feature = page.features.find((item) => item.id === featureId);
          if (feature) {
            return feature;
          }
        }
      }
    }

    return null;
  }

  private findContributor(contributorId: string) {
    return this.contributors().find((item) => item.id === contributorId) ?? null;
  }

  private getNormalizedSources(): GenerationSourceInput[] {
    return this.sourceArray.controls
      .map((control) => {
        const value = control.getRawValue() as Record<string, string>;
        const hasContent = [
          value['label'],
          value['filename'],
          value['mimeType'],
          value['contentText'],
          value['dataUrl'],
          value['url'],
          value['notes'],
        ].some((entry) => String(entry ?? '').trim().length > 0);

        if (!hasContent) {
          return null;
        }

        const derivedLabel = this.deriveSourceLabel(value);
        const normalized: GenerationSourceInput = {
          kind: (value['kind'] as SourceKind) || 'note',
          label: derivedLabel,
        };

        const filename = value['filename'].trim();
        const mimeType = value['mimeType'].trim();
        const contentText = value['contentText'].trim();
        const dataUrl = value['dataUrl'].trim();
        const url = value['url'].trim();
        const notes = value['notes'].trim();

        if (filename) {
          normalized.filename = filename;
        }

        if (mimeType) {
          normalized.mimeType = mimeType;
        }

        if (contentText) {
          normalized.contentText = contentText;
        }

        if (dataUrl) {
          normalized.dataUrl = dataUrl;
        }

        if (url) {
          normalized.url = url;
        }

        if (notes) {
          normalized.notes = notes;
        }

        return normalized;
      })
      .filter((item): item is GenerationSourceInput => item !== null);
  }

  private deriveSourceLabel(value: Record<string, string>) {
    const explicitLabel = value['label']?.trim();
    if (explicitLabel) {
      return explicitLabel;
    }

    const filename = value['filename']?.trim();
    if (filename) {
      return filename;
    }

    const contentText = value['contentText']?.trim();
    if (contentText) {
      return `${this.sourceKindMeta((value['kind'] as SourceKind) ?? 'note').label} excerpt`;
    }

    const notes = value['notes']?.trim();
    if (notes) {
      return `${this.sourceKindMeta((value['kind'] as SourceKind) ?? 'note').label} notes`;
    }

    return this.sourceKindMeta((value['kind'] as SourceKind) ?? 'note').label;
  }

  private resolveExistingProjectName() {
    const directProject = this.selectedProject()?.name?.trim();
    if (directProject) {
      return directProject;
    }

    const moduleId = this.form.controls.moduleId.value;
    if (moduleId) {
      for (const project of this.projectHierarchy()) {
        if (project.modules.some((module) => module.id === moduleId)) {
          return project.name.trim();
        }
      }
    }

    const pageId = this.form.controls.pageId.value;
    if (pageId) {
      for (const project of this.projectHierarchy()) {
        if (project.modules.some((module) => module.pages.some((page) => page.id === pageId))) {
          return project.name.trim();
        }
      }
    }

    return '';
  }

  private resolveExistingModuleName() {
    const directModule = this.selectedModule()?.name?.trim();
    if (directModule) {
      return directModule;
    }

    const pageId = this.form.controls.pageId.value;
    if (pageId) {
      for (const project of this.projectHierarchy()) {
        for (const module of project.modules) {
          if (module.pages.some((page) => page.id === pageId)) {
            return module.name.trim();
          }
        }
      }
    }

    return '';
  }

  private resolveExistingPageName() {
    return this.selectedPage()?.name?.trim() ?? '';
  }

  private resolveExistingFeatureName() {
    return this.selectedFeature()?.name?.trim() ?? '';
  }

  private syncFeatureScopeState() {
    if (!this.effectiveFeatureName().trim()) {
      return;
    }

    if (this.featureArray.length) {
      this.featureArray.clear();
    }

    if (this.featureDraft.value.trim()) {
      this.featureDraft.setValue('');
    }
  }

